/**
 * WHO Child Growth Standards (2006) — 0 to 60 mois
 * WHO Reference 2007 — 5 to 18 ans
 *
 * Percentiles : P3, P15, P50, P85, P97
 * Sources :
 *   - Weight-for-age, Length/Height-for-age, Head circumference-for-age
 *   - https://www.who.int/tools/child-growth-standards/standards
 *   - https://www.who.int/tools/growth-reference-data-for-5to19-years
 *
 * Unites : poids en kg, taille en cm, perimetre cranien en cm
 */

export interface GrowthPercentiles {
  /** Age en mois */
  months: number[];
  /** Valeurs au 3e percentile */
  p3: number[];
  /** Valeurs au 15e percentile */
  p15: number[];
  /** Valeurs au 50e percentile (mediane) */
  p50: number[];
  /** Valeurs au 85e percentile */
  p85: number[];
  /** Valeurs au 97e percentile */
  p97: number[];
}

export interface GrowthReference {
  boys: {
    weight: GrowthPercentiles;   // kg, 0-60 mois
    height: GrowthPercentiles;   // cm, 0-60 mois
    head: GrowthPercentiles;     // cm, 0-36 mois
  };
  girls: {
    weight: GrowthPercentiles;
    height: GrowthPercentiles;
    head: GrowthPercentiles;
  };
}

export interface GrowthReferenceOlder {
  boys: {
    weight: GrowthPercentiles; // kg, 5-18 ans (mois = 60,72,...216)
    height: GrowthPercentiles; // cm, 5-18 ans
  };
  girls: {
    weight: GrowthPercentiles;
    height: GrowthPercentiles;
  };
}

