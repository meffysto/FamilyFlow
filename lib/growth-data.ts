/**
 * Retourne les donnees de reference de croissance WHO pour un enfant
 * selon son sexe et la metrique souhaitee.
 *
 * Pour le perimetre cranien, seules les donnees 0-36 mois sont disponibles.
 * Pour poids et taille, les donnees 0-60 mois et 5-18 ans sont fusionnees.
 */
export function getGrowthReference(
  sex: 'garçon' | 'fille',
  metric: 'weight' | 'height' | 'head',
): GrowthPercentiles | null {
  const key = sex === 'garçon' ? 'boys' : 'girls';

  if (metric === 'head') {
    return WHO_GROWTH_DATA[key].head;
  }

  // Fusionner les donnees 0-60 mois avec 5-18 ans (en evitant le doublon a 60 mois)
  const young = WHO_GROWTH_DATA[key][metric];
  const older = WHO_GROWTH_DATA_OLDER[key][metric];

  // Le mois 60 est present dans les deux jeux — on garde celui de WHO 2006
  // et on ajoute les mois > 60 depuis WHO 2007
  const olderStartIndex = older.months.findIndex((m) => m > 60);
  if (olderStartIndex === -1) {
    return young;
  }

  return {
    months: [...young.months, ...older.months.slice(olderStartIndex)],
    p3: [...young.p3, ...older.p3.slice(olderStartIndex)],
    p15: [...young.p15, ...older.p15.slice(olderStartIndex)],
    p50: [...young.p50, ...older.p50.slice(olderStartIndex)],
    p85: [...young.p85, ...older.p85.slice(olderStartIndex)],
    p97: [...young.p97, ...older.p97.slice(olderStartIndex)],
  };
}

/**
 * Interpole lineairement une valeur sur une courbe de percentile
 * pour un age donne en mois (supporte les valeurs non-entieres).
 */
export function interpolatePercentile(
  data: GrowthPercentiles,
  percentile: 'p3' | 'p15' | 'p50' | 'p85' | 'p97',
  ageMonths: number,
): number | null {
  const { months } = data;
  const values = data[percentile];

  if (ageMonths < months[0] || ageMonths > months[months.length - 1]) {
    return null;
  }

  // Trouver l'intervalle
  for (let i = 0; i < months.length - 1; i++) {
    if (ageMonths >= months[i] && ageMonths <= months[i + 1]) {
      const t = (ageMonths - months[i]) / (months[i + 1] - months[i]);
      return Math.round((values[i] + t * (values[i + 1] - values[i])) * 10) / 10;
    }
  }

  // Age exact au dernier point
  if (ageMonths === months[months.length - 1]) {
    return values[values.length - 1];
  }

  return null;
}

/**
 * Estime le percentile approximatif d'une mesure donnee
 * par rapport aux courbes de reference WHO.
 * Retourne un nombre entre 0 et 100.
 */
export function estimatePercentile(
  data: GrowthPercentiles,
  ageMonths: number,
  value: number,
): number | null {
  const percentiles = [
    { key: 'p3' as const, pct: 3 },
    { key: 'p15' as const, pct: 15 },
    { key: 'p50' as const, pct: 50 },
    { key: 'p85' as const, pct: 85 },
    { key: 'p97' as const, pct: 97 },
  ];

  const interpolated = percentiles.map((p) => ({
    pct: p.pct,
    val: interpolatePercentile(data, p.key, ageMonths),
  }));

  // Verifier que toutes les interpolations sont valides
  if (interpolated.some((p) => p.val === null)) {
    return null;
  }

  const points = interpolated as { pct: number; val: number }[];

  // En dessous du P3
  if (value <= points[0].val) {
    return Math.max(0, Math.round(3 * (value / points[0].val)));
  }

  // Au-dessus du P97
  if (value >= points[points.length - 1].val) {
    return Math.min(100, Math.round(97 + 3 * ((value - points[points.length - 1].val) / points[points.length - 1].val) * 100));
  }

  // Interpolation lineaire entre les percentiles connus
  for (let i = 0; i < points.length - 1; i++) {
    if (value >= points[i].val && value <= points[i + 1].val) {
      const t = (value - points[i].val) / (points[i + 1].val - points[i].val);
      return Math.round(points[i].pct + t * (points[i + 1].pct - points[i].pct));
    }
  }

  return null;
}
